float soma(float num1, float num2, float resultado){//pega o num1 e soma com o num2
    resultado = num1 + num2;
}
float subtracao(float num1, float num2, float resultado){//pega o num1 e o subtrai com o num2
    resultado = num1 - num2;
}
float divisao(float num1, float num2, float resultado){//pega o num1 e o divide com o num2
    resultado = num1 / num2;
}
float multiplicacao(float num1, float num2, float resultado){//pega o num1 e o multiplica com o num2
    resultado = num1 * num2;
}